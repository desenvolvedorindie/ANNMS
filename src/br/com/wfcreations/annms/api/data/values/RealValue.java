package br.com.wfcreations.annms.api.data.values;

import br.com.wfcreations.annms.api.data.IValue;

public class RealValue implements IValue {

	private static final long serialVersionUID = 1L;

	public static double getValueFor(IValue value) {
		return (double) value.getValue();
	}

	private final double value;

	public RealValue() {
		value = 0;
	}

	public RealValue(double value) {
		this.value = value;
	}

	@Override
	public Double getValue() {
		return this.value;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		RealValue other = (RealValue) obj;
		if (Double.doubleToLongBits(value) != Double.doubleToLongBits(other.value))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return String.valueOf(this.value);
	}
}
